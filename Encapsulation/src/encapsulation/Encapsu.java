
package encapsulation;


public class Encapsu {
    private String name;

    public Encapsu(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public void display()
    {
        System.out.println(name);
    }
    
    
    
    
    
}
